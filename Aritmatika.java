public class Aritmatika {
    public static void main(String[] args) {
        int a = 25;
        double b = 16;
        int c = 10;

        System.out.println ("Penjumlahan :" + (a + b + c));
        System.out.println ("Pengurangan :" + (a - b - c));
        System.out.println ("Perkalian :" + (a * b * c));
        System.out.println ("Pembagian :" + (a / b / c));
    }
}